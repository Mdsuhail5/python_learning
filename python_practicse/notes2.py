# variables: containers used to store data values
a = 2
# where a is the variable and =(assignment operator) and 2 is the value


# Assign multiple values to multiple variables
x, y, z = 10, 20, 30
print(x, y, z, sep="\n")

# Assign one value to multiple variables
b = c = d = 10
print(b, c, d)

